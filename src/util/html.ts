/** Escape the five HTML-significant characters so untrusted text can be safely
 * interpolated into the DialogV2 `content` strings we build by concatenation. */
export function escapeHtml(s: string): string {
  return s.replace(
    /[&<>"']/g,
    (c) =>
      ({
        "&": "&amp;",
        "<": "&lt;",
        ">": "&gt;",
        '"': "&quot;",
        "'": "&#39;",
      })[c]!,
  );
}
